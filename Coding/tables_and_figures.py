# 6. Figure 3: Common Support (Overlap) Density Plot
# ---------------------------------------------------------
print("Generating Figure 3: Common Support Density Plot...")
# A quick pass to get propensity scores without re-running the full DML loop
# We use the Elastic Net classifier since it is fast and performed well

# 1. Load data
df = pd.read_csv('Data/urban_emissions_panel_cleaned.csv')
exclude_from_X = ['city_id', 'year', 'log_transport_co2', 'log_total_co2', 'cp_active', 'lez_active', 'cp_impl_year', 'lez_impl_year', 'cp_announce_year', 'lez_announce_year', 'country_id', 'cp_x_lez']
numeric_df = df.select_dtypes(include=[np.number])
X_cols = [col for col in numeric_df.columns if col not in exclude_from_X]
X = df[X_cols]
D = df['cp_active'] # Plotting overlap for Congestion Pricing as the example

# 2. Fast Propensity Score Estimation
clf = make_pipeline(StandardScaler(), LogisticRegressionCV(cv=3, l1_ratios=[0.5], solver='saga', random_state=42, n_jobs=5))
propensity_scores = cross_val_predict(clf, X, D, cv=5, method='predict_proba')[:, 1]

# 3. Plot Density
fig, ax = plt.subplots(figsize=(8, 5))

sns.kdeplot(propensity_scores[D == 1], fill=True, label="Treated (CP Active)", color="#d95f02", ax=ax, alpha=0.5)
sns.kdeplot(propensity_scores[D == 0], fill=True, label="Untreated (No CP)", color="#7570b3", ax=ax, alpha=0.5)

ax.set_xlim(0, 1)
ax.set_xlabel("Estimated Propensity Score $\\hat{P}(D=1|X)$")
ax.set_ylabel("Density")
ax.set_title("Figure 3: Common Support for Congestion Pricing", pad=15, fontweight='bold')
ax.legend()

plt.tight_layout()
fig.savefig(figures_dir / 'overlap_density.pdf', format='pdf')
plt.close()

