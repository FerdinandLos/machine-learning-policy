# Helper function to format coefficients and add significance stars
def format_estimate(coef, pval):
    if pd.isna(coef) or pd.isna(pval):
        return "--" # Handles overlap violations gracefully
    
    stars = ""
    if pval < 0.01:
        stars = "^{***}"
    elif pval < 0.05:
        stars = "^{**}"
    elif pval < 0.10:
        stars = "^{*}"
        
    return f"{coef:.4f}{stars} (p={pval:.3f})"

