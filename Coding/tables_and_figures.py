# Back out Standard Errors from p-values for the error bars
def approx_se(coef, pval):
    if pd.isna(coef) or pd.isna(pval): 
        return 0
    p = max(pval, 1e-15) # Prevent division by zero for p=0.0
    z = np.abs(norm.ppf(p / 2))
    return abs(coef) / z

dml_core['IRM_ATET_se'] = dml_core.apply(lambda row: approx_se(row['IRM_ATET_coef'], row['IRM_ATET_pval']), axis=1)

